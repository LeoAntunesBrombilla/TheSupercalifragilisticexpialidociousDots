        counter=$((counter+1))
        # echo "counter = $counter"

        if [[ $counter = $apps2_length ]]; then
            read -p "Type here: " u_input

            counter0=0
            for key in "${!apps2[@]}"; do         # key = app

                just_nums=$(echo $key | sed 's/[^0-9]*//g')

                if [[ "$u_input" == "$just_nums" ]]; then
                    cmd1="${apps2[$key]}"
                    # echo -e "\ncmd1 = $cmd1"
                    eval "$cmd1"
