function ProgressBar() {    #progress bar indicator
    # Process data
        let _progress=(${1}*100/${2}*100)/100
        let _done=(${_progress}*4)/10
        let _left=40-$_done
    # Build progressbar string lengths
        _fill=$(printf "%${_done}s")
        _empty=$(printf "%${_left}s")

    # 1.2 Build progressbar strings and print the ProgressBar line
    # 1.2.1 Output example:                           
    # 1.2.1.1 Progress : [########################################] 100%
    printf "\rProgress : [${_fill// /$hashtag}${_empty// /-}] ${_progress}%%"
}

function add_ppas() {

    read -t 10 -n 1 -s -r -p "'${lines_PPAs}' PPA pkgs will be added, press any key to continue..."

    counter=1

    echo -e "\n\n\n###########################################################\n\n"

    while read ppa; do
        echo "ðŸ’¡----------Checking if $ppa is already added..."

        if ! grep -q "^deb .*$ppa" /etc/apt/sources.list /etc/apt/sources.list.d/*; then
            # commands to add the ppa...
            echo "ðŸ’¡----------$ppa is not added. Adding it now..."
            `sudo add-apt-repository ppa:$ppa`
            echo -e "\nðŸŽ‰'$ppa' was successfully added!ðŸŽ‰\n\n"
        else
            echo "ðŸ’¡----------$ppa is already added. Skipping it..."
        fi

        ProgressBar ${counter} ${lines_PPAs}
        echo -e "\n"

        counter=$((counter+1))
    done < $PPAs

    echo ""
}

